    public void load(){
        String user=studentlogin.suser.getText();
        String pass=studentlogin.spass.getText();
        String id;
        try {
            pst = con.prepareStatement("select id from student where stname= ? and spass= ?");
            pst.setString(1,user);
            pst.setString(2,pass);
            rs = pst.executeQuery();
            if(rs.next()){
            id= rs.getString("id");
            sid.setText(id);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(studentpage.class.getName()).log(Level.SEVERE, null, ex);
        }
        name.setText(studentlogin.suser.getText());
        int tm= Test.marks;
        Lmarks.setText(tm+" marks");
        
    }
    
