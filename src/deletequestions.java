    public void load(){
        
        try {
            pst =con.prepareStatement("select * from examquestion");
            rs= pst.executeQuery();
            
            ResultSetMetaData rsd= rs.getMetaData();
            int c;
            c= rsd.getColumnCount();
            DefaultTableModel d= (DefaultTableModel)dqt.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
                Vector v = new Vector();
                for(int i=0; i<=c; i++){
                v.add(rs.getString("id"));
                v.add(rs.getString("questions"));
                v.add(rs.getString("op1"));
                v.add(rs.getString("op2"));
                v.add(rs.getString("op3"));  
                v.add(rs.getString("op4"));
                v.add(rs.getString("ans"));
                }
                
                d.addRow(v);
            }  
        } catch (SQLException ex) {
            Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

