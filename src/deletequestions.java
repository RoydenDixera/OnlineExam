    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        String Question=txtQues.getText();
        String option1=txtop1.getText();
        String option2=txtop2.getText();
        String option3=txtop3.getText();
        String option4=txtop4.getText();
        String answer=txtans.getText();
        if(Question.equals("")|| option1.equals("")|| option2.equals("")|| option3.equals("")|| option4.equals("")|| answer.equals("")){
            JOptionPane.showMessageDialog(this, "Input Data");
        }else{
            try {
          
            DefaultTableModel d= (DefaultTableModel)dqt.getModel();
            int selectIndex =dqt.getSelectedRow();
            String id = d.getValueAt(selectIndex,0).toString();
            
            
            pst =con.prepareStatement("update examquestion set questions=?,op1=?,op2=?,op3=?,op4=?,ans=? where id = ?");
            pst.setString(1,Question);
            pst.setString(2,option1);
            pst.setString(3,option2);
            pst.setString(4,option3);
            pst.setString(5,option4);
            pst.setString(6,answer);
            pst.setString(7,id);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record updated");
                txtQues.setText("");
                txtop1.setText("");
                txtop2.setText("");
                txtop3.setText("");
                txtop4.setText("");
                txtans.setText("");
                load();
            }else{
                JOptionPane.showMessageDialog(this, "Record update failed");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
        }}    }//GEN-LAST:event_editActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
            try {
            DefaultTableModel d= (DefaultTableModel)dqt.getModel();
            int selectIndex =dqt.getSelectedRow();
            String id = d.getValueAt(selectIndex,0).toString();
            
            
            pst =con.prepareStatement("delete from examquestion where id = ?");
            pst.setString(1,id);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record Deleted");
                txtQues.requestFocus();
                load();
            }else{
                JOptionPane.showMessageDialog(this, "Record deletion failed");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(student.class.getName()).log(Level.SEVERE, null, ex);
        }    }//GEN-LAST:event_deleteActionPerformed

